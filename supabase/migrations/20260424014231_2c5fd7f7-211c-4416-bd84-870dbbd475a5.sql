-- Tabla de corridas de revaluación
CREATE TABLE IF NOT EXISTS public.tab_fx_revaluation_runs (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  enterprise_id bigint NOT NULL,
  year integer NOT NULL,
  month integer NOT NULL,
  cutoff_date date NOT NULL,
  revaluation_type text NOT NULL DEFAULT 'UNREALIZED', -- UNREALIZED | REALIZED
  journal_entry_id bigint REFERENCES public.tab_journal_entries(id) ON DELETE SET NULL,
  total_gain numeric(18,2) NOT NULL DEFAULT 0,
  total_loss numeric(18,2) NOT NULL DEFAULT 0,
  details_json jsonb,
  status text NOT NULL DEFAULT 'POSTED', -- DRAFT | POSTED | REVERSED
  created_by uuid DEFAULT auth.uid(),
  created_at timestamptz NOT NULL DEFAULT now(),
  reversed_at timestamptz,
  notes text
);

CREATE UNIQUE INDEX IF NOT EXISTS idx_fx_reval_runs_unique
  ON public.tab_fx_revaluation_runs (enterprise_id, year, month, revaluation_type)
  WHERE status = 'POSTED';

ALTER TABLE public.tab_fx_revaluation_runs ENABLE ROW LEVEL SECURITY;

CREATE POLICY "fx_reval_runs_select" ON public.tab_fx_revaluation_runs
  FOR SELECT USING (
    is_super_admin(auth.uid()) OR user_is_linked_to_enterprise(auth.uid(), enterprise_id)
