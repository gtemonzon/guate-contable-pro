CREATE POLICY "fx_reval_runs_update" ON public.tab_fx_revaluation_runs
  FOR UPDATE USING (
    is_super_admin(auth.uid()) OR user_is_linked_to_enterprise(auth.uid(), enterprise_id)
  );

-- Función: saldo de una cuenta en moneda extranjera a una fecha (suma original_debit - original_credit por moneda)
CREATE OR REPLACE FUNCTION public.get_account_fx_balance(
  _account_id bigint,
  _currency_code text,
  _cutoff_date date
)
RETURNS numeric
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT COALESCE(SUM(
    COALESCE(d.original_debit, d.debit_amount, 0)
    - COALESCE(d.original_credit, d.credit_amount, 0)
  ), 0)
  FROM public.tab_journal_entry_details d
  JOIN public.tab_journal_entries j ON j.id = d.journal_entry_id
  WHERE d.account_id = _account_id
    AND j.entry_date <= _cutoff_date
    AND j.status = 'contabilizada'
    AND COALESCE(d.currency_code, j.currency_code, 'GTQ') = _currency_code;
$$;

COMMENT ON FUNCTION public.get_account_fx_balance IS 'Calcula el saldo vivo de una cuenta monetaria expresado en su moneda extranjera, sumando original_debit/credit de partidas contabilizadas hasta la fecha de corte.';